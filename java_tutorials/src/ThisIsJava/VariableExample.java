// 변수 선언 초기화
package ThisIsJava;

public class VariableExample {
  VariableExample () {
	  int value = 10;  // 변수에 10을 저장
	  
	  int result = value + 10; //변수값을 초기화 후 다시 값을 저장
	  
	  System.out.println(result);
	  
  }
  public static void main(String[] args) {
	  new VariableExample();
  }
}
